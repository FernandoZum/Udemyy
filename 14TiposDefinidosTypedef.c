//Inclusion de librerias
#include <stdio.h>
#include <stdlib.h>

//Se iniciara el programa
int main()
{
    //Se declara la variable por tipo de dato int
    int valor;
    
    //typedef funciona como estructuras de datos de tipo primitivo
    typedef int entero;

    //Con ello no se necesitara el int para determinar una variable
    entero valor1;
    entero valor2;

    //Lo mismo funciona con typedef long para poder crear una estructura larga
    typedef long entero_largo;

    //Nos servira para definir una variable
    entero_largo valor3;

}
